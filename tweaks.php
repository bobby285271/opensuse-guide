<div class="os3">1、为什么Fcitx中文输入法候选字不能翻页？</div>
Fcitx中文输入法的候选字翻页的快捷键是“-”和“+”按键（不需要按Shift），所以按PageUp和PageDown键是不能翻页的。可以自己配置Fcitx中文输入法翻页的快捷键。
<br /><br />
<div class="os3">2、为什么开机时屏幕花屏/黑屏或无法正常启动到图形桌面？</div>
可能原因：默认的开源驱动对部分显卡支持得不够好，可能会花屏。
如果出现花屏，建议进入openSUSE高级启动界面里的openSUSE修复模式，
然后按照“显卡驱动”教程页面安装Nvidia或AMD对应的专有显卡驱动。<br/>
另外，如果之前在12.2版本系统安装了N卡或A卡的专有显卡驱动，
然后通过update在线升级到12.3版本，如果升级后出现无法启动到图形桌面的情况，
那么同样进入openSUSE修复模式，重新安装12.3版本的N卡或A卡的专有显卡驱动。
<br /><br />

<div class="os3">3、为什么硬盘安装openSUSE时出错，提示 no repository found ？</div>
openSUSE 12.2 和 12.3 版本硬盘安装时都有这个 bug ，即使选了正确的磁盘分区和正确的路径文件名，还是会提示 no repository found 。解决方法：<br />
返回（Back）到Main Menu界面；<br />
找到 Expert 条目，进入；<br />
找到 Start Shell 条目，打开后进入命令行，执行：<br />
<div class="cl">cp  /usr/bin/mount  /bin</div><br />
再执行 exit ，返回到Main Menu界面，然后进行正常的硬盘安装。

<br /><br />

<div class="os3">4、为什么打开Windows下的文本文件出现中文汉字乱码？</div>
因为Linux默认的中文文本是Unicode编码，
简体中文Windows默认中文文本编码是国标码GB2312和GB18030，
所以打开Windows下的文本文件可能出现汉字乱码。<br />
解决方法是设置编辑器的文本编码方式。以KWrite为例，打开windows下的xx.txt，
如果汉字乱码了，选择KWrite的菜单：<br />
“工具”---->“编码”---->“简体中文”---->“GB18030”，<br />
然后显示简体中文就正常了。<br />
如果要让编辑器默认就打开GB18030的文本，
选择菜单：“设置”---->“配置编辑器”，打开设置对话框，
在对话框左边一栏选“打开和保存”，
右边会看到“通用”项，里面“文件格式”---->“编码”，
从列表里选择“简体中文GB18030”，保存配置。
然后点击xx.txt，默认就是国标码打开，不会乱码了。

<br /><br />

<div class="os3">5、为什么打开音乐文件MP3时播放器显示中文汉字乱码？</div>
原因是国内几乎所有MP3文件的内置标签文本是GB2312（GBK）编码的，
而Linux下播放器（Amarok、Audacious等）默认以Unicode编码（UTF-8、UTF-16）
打开MP3文件的内置标签文本，就会出现中文汉字乱码。<br />
解决方法是将MP3内置标签编码转换成Unicode。
Linux下可以用python-mutagen来转换：<br />
（1）首先在Linux下安装python-mutagen（audio metadata editing library）<br />
<div class="clroot">
zypper  in  python-mutagen
</div> <br />
（2）进行标签编码转换<br />
在当前目录和所有子目录搜索MP3文件并转换其标签编码：<br />
<div class="cl">
find . -iname "*.mp3" -execdir mid3iconv --remove-v1 -e gbk {} \;
</div> <br />
对应在Windows下也有软件（ConvertZ）可以转换标签编码，参考：<br />
<a href="http://jingyan.baidu.com/article/b2c186c8810535c46ef6ffd4.html" target="_blank">
批量转换mp3上的id3标签教程
</a>

